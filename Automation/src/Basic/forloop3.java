
package Basic;

public class forloop3 {

	public static void main(String[]args)
	{
		
		
		for (int a=0; a<=5; a++)
		{
			for (int j=0; j<=a; j++)
				{
				System.out.print(" ");
				}
			
			for (int k=5; k>=a; k--)
			{
				System.out.print(" *");
			}
			for (int j=0; j<=a; j++)
			{
			System.out.print(" "	+ " ");
			}
		
		for (int k=5; k>=a; k--)
		{
			System.out.print(" *");
		}
			System.out.println();
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
