
package Basic;

public class forlooppratice {
	public static void main ( String []args)
	{
		for (int a=1; a<=5 ; a++ ) 
		{
				System.out.print( a + " ");
		}
		System.out.print("  "); 
		
		for (int a=5; a>=1 ; a-- ) 
		{
				System.out.print( a + " ");
		}
		System.out.println("  "); 
		
		
		for (int b=1; b<=4; b++)
		{
			System.out.print(" "+ b);
		}
		System.out.print("     "); 
		
		for (int b=4; b>=1; b--)
		{
			System.out.print( b + " ");
		}
		System.out.println("     "); 
		
		for (int b=1; b<=3; b++)
		{
			System.out.print(" "+ b);
		}
		System.out.print("       "); 
		
		for (int b=3; b>=1; b--)
		{
			System.out.print( b + " ");
		}
		System.out.println("       "); 
		 
		
	
	
	
	
	
	
	
	
	}
}









